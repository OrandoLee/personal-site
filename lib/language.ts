const reverseCharMap = Object.fromEntries(
  Object.entries(charMap).map(([simplified, traditional]) => [
    traditional,
    simplified
  ])
) as Record<string, string>;

const htmlEntityPattern = /&(?:[a-z]+|#[0-9]+|#x[0-9a-f]+);/gi;
const protectedSegmentPattern =
  /(`[^`]*`|https?:\/\/[^\s<>"']+|\/[A-Za-z0-9._~:/?#[\]@!$&'()*+,;=%-]+|[A-Za-z][A-Za-z0-9._:+/#@-]*(?:\s+[A-Za-z][A-Za-z0-9._:+/#@-]*)*)/g;

function applyPhrasePairs(text: string, pairs: ReadonlyArray<readonly [string, string]>) {
  let next = text;

  for (const [from, to] of pairs) {
    next = next.split(from).join(to);
  }

  return next;
}

function convertCharacters(text: string, map: Record<string, string>) {
  let next = "";

  for (const character of text) {
    next += map[character] ?? character;
  }

  return next;
}

function convertChineseSegment(text: string, language: SiteLanguage) {
  if (language === "zh-Hans") {
    return convertCharacters(
      applyPhrasePairs(text, traditionalToSimplifiedPhrases),
      reverseCharMap
    );
  }

  return convertCharacters(applyPhrasePairs(text, sortedPhrasePairs), charMap);
}

function convertOutsideProtectedSegments(text: string, language: SiteLanguage) {
  let cursor = 0;
  let converted = "";
  protectedSegmentPattern.lastIndex = 0;
  let match = protectedSegmentPattern.exec(text);

  while (match) {
    const index = match.index ?? 0;
    converted += convertChineseSegment(text.slice(cursor, index), language);
    converted += match[0];
    cursor = index + match[0].length;
    match = protectedSegmentPattern.exec(text);
  }

  converted += convertChineseSegment(text.slice(cursor), language);
  return converted;
}

export function translateText(text: string, language: SiteLanguage) {
  if (!/[\u3400-\u9fff]/.test(text)) {
    return text;
  }

  let cursor = 0;
  let converted = "";
  htmlEntityPattern.lastIndex = 0;
  let match = htmlEntityPattern.exec(text);

  while (match) {
    const index = match.index ?? 0;
    converted += convertOutsideProtectedSegments(text.slice(cursor, index), language);
    converted += match[0];
    cursor = index + match[0].length;
    match = htmlEntityPattern.exec(text);
  }

  converted += convertOutsideProtectedSegments(text.slice(cursor), language);
  return converted;
}
